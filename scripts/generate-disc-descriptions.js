import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const DISCS_FILE = path.resolve(__dirname, '../public/data/discs_fallback.json');
const LM_STUDIO_URL = 'http://192.168.1.153:1234/v1/chat/completions';

async function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function main() {
  if (!fs.existsSync(DISCS_FILE)) {
    console.error('discs_fallback.json not found!');
    process.exit(1);
  }

  const discs = JSON.parse(fs.readFileSync(DISCS_FILE, 'utf-8'));
  
  // Find discs missing descriptions
  const missing = discs.filter(d => !d.description || d.description.trim() === '');
  console.log(`Audit Results:`);
  console.log(`- Total Discs: ${discs.length}`);
  console.log(`- Has Description: ${discs.length - missing.length}`);
  console.log(`- Missing Description: ${missing.length}`);
  
  if (missing.length === 0) {
    console.log('All discs have descriptions! Exiting.');
    return;
  }

  console.log('\nBeginning automated description generation using local LM Studio...');
  
  let updatedCount = 0;

  for (let i = 0; i < missing.length; i++) {
    const disc = missing[i];
    console.log(`[${i+1}/${missing.length}] Generating for ${disc.brand} ${disc.name}...`);
    
    const prompt = `Write a highly accurate, engaging, and SEO-optimized 2-3 sentence product description for the disc golf disc: "${disc.brand} ${disc.name}".
Flight numbers are: Speed ${disc.speed}, Glide ${disc.glide}, Turn ${disc.turn}, Fade ${disc.fade}. Category: ${disc.category}.

Rules:
1. Do NOT include greetings, headers, or any other text. 
2. Return ONLY the description text itself.
3. Be accurate to how this specific disc actually flies in real life. If it is famous for something (e.g., the Buzzz being a straight midrange, the Firebird being heavily overstable), mention it.`;

    try {
      const response = await fetch(LM_STUDIO_URL, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          model: 'llama3.3-8b-instruct-thinking-heretic-uncensored-claude-4.5-opus-high-reasoning-i1',
          messages: [
            { role: 'system', content: 'You are an expert disc golf writer and professional player.' },
            { role: 'user', content: prompt }
          ],
          temperature: 0.7,
        })
      });

      if (!response.ok) {
        throw new Error(`HTTP error! status: ${response.status}`);
      }

      const data = await response.json();
      // Remove any `<think>...</think>` tags that might be generated by reasoning models
      let description = data.choices[0].message.content.trim();
      description = description.replace(/<think>[\s\S]*?<\/think>\n*/g, '').trim();
      
      // Update the disc object reference
      disc.description = description;
      updatedCount++;

      // Save every 10 iterations to not lose progress
      if (updatedCount % 10 === 0) {
        fs.writeFileSync(DISCS_FILE, JSON.stringify(discs, null, 2));
        console.log(`Saved progress (${updatedCount} updated so far)...`);
      }

      // Add a small delay
      await sleep(100);
      
    } catch (error) {
      console.error(`Error generating for ${disc.name}:`, error.message);
      // Back off a bit on error
      await sleep(2000);
    }
  }

  // Final save
  fs.writeFileSync(DISCS_FILE, JSON.stringify(discs, null, 2));
  console.log(`\nSuccessfully generated and saved ${updatedCount} descriptions!`);
}

main().catch(console.error);
